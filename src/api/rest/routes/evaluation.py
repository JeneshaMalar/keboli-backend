"""Evaluation routes for managing evaluation reports and admin decisions."""

import uuid

from fastapi import APIRouter, Depends, status
from sqlalchemy.ext.asyncio import AsyncSession

from src.api.rest.dependencies import get_current_recruiter, get_db, CurrentRecruiter
from src.core.services.evaluation_service import EvaluationService
from src.schemas.evaluation_schema import (
    EvaluationCreate,
    EvaluationReportResponse,
    EvaluationResponse,
    EvaluationUpdate,
)

router = APIRouter(prefix="/evaluation", tags=["evaluation"])


@router.post(
    "/trigger/{session_id}",
    response_model=dict,
    status_code=status.HTTP_202_ACCEPTED,
    summary="Trigger evaluation",
    description="Trigger the external evaluation service for a completed interview session.",
)
async def trigger_evaluation(
    session_id: uuid.UUID,
    db: AsyncSession = Depends(get_db),
) -> dict[str, str]:
    """Trigger the external evaluation pipeline for a completed session.

    Args:
        session_id: UUID of the interview session.
        db: Async database session.

    Returns:
        Status confirmation with the session ID.
    """
    service = EvaluationService(db)
    return await service.trigger_evaluation(session_id)


@router.post(
    "/{session_id}",
    response_model=EvaluationResponse,
    status_code=status.HTTP_201_CREATED,
    summary="Save evaluation",
    description="Persist an evaluation report generated by the evaluation service.",
)
async def save_evaluation(
    session_id: uuid.UUID,
    payload: EvaluationCreate,
    db: AsyncSession = Depends(get_db),
) -> EvaluationResponse:
    """Save an evaluation report for a completed interview session.

    Args:
        session_id: UUID of the interview session.
        payload: Evaluation scores and metadata.
        db: Async database session.

    Returns:
        The persisted evaluation response.
    """
    service = EvaluationService(db)
    evaluation = await service.save_evaluation(session_id, payload.model_dump())
    return EvaluationResponse.model_validate(evaluation)


@router.get(
    "/report/{session_id}",
    response_model=EvaluationReportResponse,
    summary="Get evaluation report",
    description="Retrieve the full evaluation report including candidate and transcript data.",
)
async def get_evaluation_report(
    session_id: uuid.UUID,
    db: AsyncSession = Depends(get_db),
    current_user: CurrentRecruiter = Depends(get_current_recruiter),
) -> EvaluationReportResponse:
    """Retrieve the full evaluation report for a session.

    Args:
        session_id: UUID of the interview session.
        db: Async database session.
        current_user: Authenticated recruiter.

    Returns:
        The evaluation report with candidate and transcript data.
    """
    service = EvaluationService(db)
    report = await service.get_evaluation_report(session_id, current_user.org_id)
    return EvaluationReportResponse(**report)


@router.patch(
    "/{session_id}",
    response_model=EvaluationResponse,
    summary="Update admin decision",
    description="Apply admin overrides to an existing evaluation (recommendation, notes, tie-winner).",
)
async def update_admin_decision(
    session_id: uuid.UUID,
    payload: EvaluationUpdate,
    db: AsyncSession = Depends(get_db),
    current_user: CurrentRecruiter = Depends(get_current_recruiter),
) -> EvaluationResponse:
    """Update an evaluation with admin overrides.

    Args:
        session_id: UUID of the interview session.
        payload: Admin override fields.
        db: Async database session.
        current_user: Authenticated recruiter.

    Returns:
        The updated evaluation response.
    """
    service = EvaluationService(db)
    evaluation = await service.update_admin_decision(
        session_id, payload.model_dump(exclude_unset=True)
    )
    return EvaluationResponse.model_validate(evaluation)


@router.get(
    "/",
    response_model=list[dict],
    summary="List organization evaluations",
    description="Retrieve all evaluations for the authenticated user's organization.",
)
async def list_evaluations(
    db: AsyncSession = Depends(get_db),
    current_user: CurrentRecruiter = Depends(get_current_recruiter),
) -> list[dict]:
    """List all evaluations for the current user's organization.

    Args:
        db: Async database session.
        current_user: Authenticated recruiter.

    Returns:
        List of evaluation summary dictionaries.
    """
    service = EvaluationService(db)
    return await service.get_org_evaluations(current_user.org_id)
